                    p_total_count AS "ARTICLES_TOTAL_COUNT",
                    0 AS "ATTACHED_FILES_COUNT"
                FROM 
                    tbl_aid r,
                    bbsarticle a
                WHERE 
                    (r.rid BETWEEN v_index_start AND v_index_end) AND
                    a.article_id = r.aid
