        p_bbs_name          IN      bbsboard.bbs_name%TYPE,
        p_bbs_type          IN      bbsboard.bbs_type%TYPE,
        p_is_enabled        IN      bbsboard.is_enabled%TYPE,
        p_result            OUT     INTEGER
    ) 
    AS
        v_count     INTEGER            := 0;
        v_errcode   INTEGER            := 0;
        v_errm      VARCHAR(4096)      := '';
    BEGIN
        SELECT COUNT(bbs_id) 
            INTO v_count
            FROM bbsboard
            WHERE bbs_id = p_bbs_id;
            
        IF v_count > 0 THEN
            UPDATE bbsboard
                SET 
                    bbs_name = p_bbs_name,
                    bbs_type = p_bbs_type,
                    is_enabled = p_is_enabled
                WHERE bbs_id = p_bbs_id;
                
            v_count := SQL%ROWCOUNT;
            
            IF v_count > 0 THEN
                COMMIT;
                v_count := 1;
            ELSE
                ROLLBACK;
                v_count := 0;
            END IF;
        ELSE
            v_count := 0;
        END IF;
        
        p_result := v_count;
        
        EXCEPTION
            WHEN OTHERS THEN
                ROLLBACK;
                p_result := -1;
                v_errcode := SQLCODE;
                v_errm := SQLERRM(v_errcode);
                DBMS_OUTPUT.PUT_LINE('ERROR : ' || v_errcode || ' ' || v_errm);
                --RAISE_APPLICATION_ERROR(v_errcode, v_errm);
    END Update_Board;

END PKG_BBS_BOARD;

